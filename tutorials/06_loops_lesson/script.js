// ===== For loop condition =====

// Happy new year 
for (i = 10; i >= 0; i--) {
    console.log(i);
}
console.log("Happy New Year!");

// Go for loop
for (i = 1; i <= 3; i++) {
    console.log(i);
}
console.log("Go");


// ===== While loop condition =====
var counter = 3;
while (counter > 0) {
    console.log(counter);
    counter = counter - 1;
}
console.log("Run")




