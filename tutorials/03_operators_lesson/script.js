// ===== Operators in javascript =====

/*
=== Arithmetic Operator ===
- Addition " + "
- Subtraction " - "
- Division " / "
- Multiplication
*/

console.log(2 + 2);     // addition
console.log(20 - 18);   // subtraction
console.log(15 / 5);    // division
console.log(7 * 12);    // multiplication
console.log(8 % 4);     // modulus

/*
=== Comparison Operator ===
- Greater than " > "
- Less than " < "
- Equal to " == "
- Not equal to " != "
*/

console.log(4 > 2);      // greater than 
console.log(10 < 20);    // less than 
console.log(10 == 10);   // equal  
console.log(50 != 100);  // not equal 

/*
=== Logical Operator ===
- And " && "
- Or " || "
- Not " ! "
*/

console.log(2 < 4 && 2 > 1); // and
console.log(2 < 10 || 2 > 20); // or
console.log(!(5 > 3)); // not