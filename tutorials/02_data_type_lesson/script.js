// ===== Data type in javascript =====

/*
- Strings
- Number
- Boolean
- Null
- Undefined
- BigInt
- Symbol
*/

// Strings 
var first_name = 'Mohamed';
var job = "Programmer";

// Printing string and type 
console.log(first_name, "is", job);
console.log(typeof (first_name));
console.log(typeof (job));

// Number 
var age = 30;
console.log(age);
console.log(typeof (age))

// Boolean 
var is_required = true
console.log(is_required);
console.log(typeof (is_required));

// Null
var discount = null
console.log(discount);
console.log(typeof (discount));

// Undefiend
var last_name;
console.log(last_name);
console.log(typeof (last_name));